function XZ=XYZ(x)
L1=0.4;%31/100;%
L2=0.325;%17/50;%
XYZ_1 = -L2 .* cos(x(:,1) + x(:,3)) - L1 .* cos(x(:,1));
XYZ_3 = L2 .* sin(x(:,1) + x(:,3)) + L1 .* sin(x(:,1));

VXZ_1 = L2 .* x(:,4) .* sin(x(:,1) + x(:,3)) + L2 .* x(:,2) .* sin(x(:,1) + x(:,3)) + L1 .* x(:,2) .* sin(x(:,1));
VXZ_3 = L2 .* x(:,4) .* cos(x(:,1) + x(:,3)) + L2 .* x(:,2) .* cos(x(:,1) + x(:,3)) + L1 .* x(:,2) .* cos(x(:,1));


XZ=[XYZ_1 XYZ_3 VXZ_1 VXZ_3];
